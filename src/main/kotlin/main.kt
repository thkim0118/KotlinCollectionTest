
fun List<String>.getShortWordsTo(shortWords: MutableList<String>, maxLength: Int) {
    this.filterTo(shortWords) { it.length <= maxLength }

    val articles = setOf("a", "A", "an", "An", "the", "The")
    shortWords -= articles
}

